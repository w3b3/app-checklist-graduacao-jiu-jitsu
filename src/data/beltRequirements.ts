// Helper function to get requirements by belt
export function getRequirementsByBelt(beltId: string): BeltRequirement[] {
  return BELT_REQUIREMENTS.filter(req => req.belt === beltId);
}

// Helper function to group requirements by category
export function groupRequirementsByCategory(requirements: BeltRequirement[]): Map<string, BeltRequirement[]> {
  const grouped = new Map<string, BeltRequirement[]>();

  requirements.forEach(req => {
    const category = req.category;
    if (!grouped.has(category)) {
      grouped.set(category, []);
    }
    grouped.get(category)!.push(req);
  });

  return grouped;
}
